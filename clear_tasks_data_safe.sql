-- 安全版数据库清理脚本
-- 只删除 tasks 表中的数据
-- 保留游戏化数据（等级、经验值、金币）

-- ============================================
-- 删除所有任务数据
-- ============================================

DELETE FROM tasks;

-- ============================================
-- 验证清理结果
-- ============================================

DO $$
DECLARE
    task_count INTEGER;
BEGIN
    SELECT COUNT(*) INTO task_count FROM tasks;
    
    IF task_count = 0 THEN
        RAISE NOTICE '✓ 任务表已清空';
    ELSE
        RAISE WARNING '警告：任务表还有 % 条记录', task_count;
    END IF;
END $$;

-- ============================================
-- 完成
-- ============================================

DO $$
BEGIN
    RAISE NOTICE '========================================';
    RAISE NOTICE '任务数据清理完成';
    RAISE NOTICE '========================================';
    RAISE NOTICE '游戏化数据（等级、经验值、金币）已保留';
    RAISE NOTICE '可以开始添加新任务了';
    RAISE NOTICE '========================================';
END $$;
